import copy
import functools


__all__ = ["deepcopy_cache"]


def deepcopy_cache(func):
    cache = dict()

    @functools.wraps(func)
    def wrapper(*args, **kwargs):
        key = (args, tuple(sorted(kwargs.items())))
        if key not in cache:
            cache[key] = func(*args, **kwargs)
        return copy.deepcopy(cache[key])
    return wrapper
